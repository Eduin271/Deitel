// Programa que imprime salida tabular.


public class Programa22{
    public static void main(String[] args){

        System.out.println("N\t N*10\t N*100\t N*1000 ");
        for(int i=1; i<=5; i++){
            System.out.printf("\n%d\t  %d\t  %d\t  %d\t", i, i*10, i*100, i*1000,"\n");
            
        }


    }

}